# 9 turtle graphics: 오륜기 그리기

import turtle
t = turtle.Pen()
t.shape("turtle")
t.width(15)
t.up()
t.goto(-220, 0)
t.color("blue")
t.down()
t.circle(100)
t.up()
t.goto(0, 0)
t.color("black")
t.down()
t.circle(100)
t.up()
t.goto(220, 0)
t.color("red")
t.down()
t.circle(100)

t.up()
t.goto(-110, -110)
t.color("yellow")
t.down()
t.circle(100)
t.up()
t.goto(110, -110)
t.color("green")
t.down()
t.circle(100)

t._screen.exitonclick()